#!/usr/bin/env bash
# Render ~/.openclaw/openclaw.json and ~/.openclaw/.env for the OU LiteLLM gateway.
# Usage: scripts/configure.sh [API_KEY]
#   API key is resolved as: $1  >  $LITELLM_API_KEY  >  existing ~/.openclaw/.env
# Idempotent: safe to run as many times as you like.
set -euo pipefail

# ---- Settings (override via env if your gateway/model differ) ----
LITELLM_BASE_URL="${LITELLM_BASE_URL:-https://litellm.lib.ou.edu}"
OPENCLAW_MODEL="${OPENCLAW_MODEL:-gemma4}"
OPENCLAW_MODEL_NAME="${OPENCLAW_MODEL_NAME:-Gemma 4 (OU LiteLLM)}"
# -----------------------------------------------------------------

STATE_DIR="${HOME}/.openclaw"
CONFIG_FILE="${STATE_DIR}/openclaw.json"
ENV_FILE="${STATE_DIR}/.env"
REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
TEMPLATE="${REPO_DIR}/config/openclaw.template.json5"
PLACEHOLDER="sk-REPLACE_ME"

mkdir -p "${STATE_DIR}"

# Resolve the API key: explicit arg > env var > existing .env file.
KEY="${1:-${LITELLM_API_KEY:-}}"
if [[ -z "${KEY}" && -f "${ENV_FILE}" ]]; then
  KEY="$(grep -E '^LITELLM_API_KEY=' "${ENV_FILE}" | tail -n1 | cut -d= -f2- || true)"
fi
[[ -z "${KEY}" ]] && KEY="${PLACEHOLDER}"

# Write ~/.openclaw/.env (OpenClaw reads this as a global env fallback).
umask 077
printf '# Auto-generated by openclaw-codespace-starter. Do NOT commit.\nLITELLM_API_KEY=%s\n' "${KEY}" > "${ENV_FILE}"
chmod 600 "${ENV_FILE}"

# Render the config from the template.
sed -e "s#@@BASE_URL@@#${LITELLM_BASE_URL}#g" \
    -e "s#@@MODEL_ID@@#${OPENCLAW_MODEL}#g" \
    -e "s#@@MODEL_NAME@@#${OPENCLAW_MODEL_NAME}#g" \
    "${TEMPLATE}" > "${CONFIG_FILE}"

echo "Wrote ${CONFIG_FILE} (model: ${OPENCLAW_MODEL}, baseUrl: ${LITELLM_BASE_URL})"

if [[ "${KEY}" == "${PLACEHOLDER}" ]]; then
  echo "!! No API key found — wrote a placeholder."
  echo "   Run 'bash scripts/set-key.sh' or set the LITELLM_API_KEY Codespaces secret."
fi

# Best-effort validation if OpenClaw is already installed.
if command -v openclaw >/dev/null 2>&1; then
  openclaw config validate >/dev/null 2>&1 \
    && echo "Config validated OK." \
    || echo "(Heads up: 'openclaw config validate' reported issues — run 'openclaw doctor' to inspect.)"
fi
